a = {1, 2, 3, 4}
b = {4, 5, 6, 7, 8}

print('Essa é a união dos conjuntos:', a | b) # | equivale a 'ou'
print('Essa é a interseção dos conjuntos:', a & b) # & equivale a 'e'
print('Esses são os números exclusivos do conjunto a:', a - b)
print('Esses são os números exclusivos do conjunto b:', b - a)
print('Esses são os números exclusivos dos conjuntos:', a ^ b)
